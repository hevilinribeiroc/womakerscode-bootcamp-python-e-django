print('Olá, essa é uma calculadora de conversão de quilômetros para metros, centímetros e milímetros.')
km = int(input('Digite a quantidade de quilômetros: '))

metro = km*1000
centimetros = km*100000
milimetros = km*1000000

print(f'{km} quilômetro(s) corresponde(m) a {metro} metro(s).')
print(f'{km} quilômetro(s) corresponde(m) a {centimetros} centímetro(s).')
print(f'{km} quilômetro(s) corresponde(m) a {milimetros} milímetro(s).')
